package monModele;

public class Tabouret {
    String dossier;
    String matiere;

    public Tabouret() {
    }

    public String getDossier() {
        return this.dossier;
    }

    public void setDossier(String dossier) {
        this.dossier = dossier;
    }

    public String getMatiere() {
        return this.matiere;
    }

    public void setMatiere(String matiere) {
        this.matiere = matiere;
    }
}